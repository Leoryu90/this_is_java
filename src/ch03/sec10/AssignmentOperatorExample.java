public class AssignmentOperatorExample {
	public static void main(String[] args) {
		int r = 0;
		System.out.println(r);
		r += 5;
		System.out.println(r);
		r *= 3;
		System.out.println(r);
		r /= 5;
		System.out.println(r);
		r += 1;
		System.out.println(r);
		r %= 3;
		System.out.println(r);
	}

}
