//클래스 선언
public class StringConcatExample {
	//프로그램 실행 진입점
	public static void main(String[] args) {
		//콘촐에 출력되는 실행문
		
		//숫자 연산
		int r1 = 10 + 2 + 8;
		System.out.println(r1);
		
		//결합 연산
		String r2 = 10 + 2 + "8";
		System.out.println(r2);
	}

}
